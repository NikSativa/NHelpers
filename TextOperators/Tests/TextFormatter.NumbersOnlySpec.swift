import Nimble
import NSpry
import Quick

@testable import NTextOperators

final class TextFormatter_NumbersOnlySpec: QuickSpec {
    override func spec() {
        describe("TextFormatter.NumbersOnly") {
            var subject: TextFormatter!

            beforeEach {
                subject = TextFormatter.numbersOnly
            }

            it("should remove all letters") {
                expect(subject.formatText("123ABV34")) == "12334"
            }

            it("should generate unique id and be equatable") {
                expect(subject) == .numbersOnly
                expect(subject) != .identity
            }
        }
    }
}
